/* Task 9
Вывести таблицу умножения для всех чисел от 2 до 9.   
Каждое число необходимо умножить на числа от 1 до 10. */


// делаем первый цикл от 1 до 10, т.к образом ш-границы талицы умножения
for (let i = 1; i <= 10; i++) {
    // выводим заголовок: Умноженея на 1,2 и т.д.
    console.log(`Multiplication on ${i}:`);

    // вторым циклом задаем множители и выводим в консоль
    for (let x = 1; x <= 10; x++) {
        console.log(`${i} * ${x} = ${i * x}`);
    }
}